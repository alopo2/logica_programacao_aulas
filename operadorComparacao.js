//Operadores de Comparação: 

// == igual a
// === igual em valor e tipo
// != diferente
// !== diferente em valor e tipo
// > maior que
// < menor que
// >= maior ou igual
// <= menor ou igual

let numero1 = 15; //tipo number (inteiro)
let numero2 = 6; //tipo number (inteiro)

//console.log (numero1 == numero2); //emite verdadeiro ou falso se os valores forem iguais
//console.log (numero1 === numero2); //emite verdadeiro se os valores forem iguais em relação ao valor e o tipo.
//console.log (numero1 != numero2); //emite verdadeiro se os valores forem diferentes entre si.
//console.log (numero1 !== numero2); //emite verdadeiro se os valores forem diferente em valor e tipo entre si.

console.log (numero1 > numero2); //emite verdadeiro se numero1 for maior que numero2 //true
console.log (numero1 < numero2); //emite verdadeiro se numero1 for menor que numero2 //false
console.log (numero1 >= numero2); //emite verdadeiro se numero1 for maior OU igual a numero2 //true
console.log (numero1 <= numero2); //emite verdadeiro se numero1 for menor OU igual a numero2 //false